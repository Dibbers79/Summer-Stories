							<h1>Digging into Urban Problems</h1>
							<p class="ec-emphasis">A rising junior is studying research methods and more at the University of Chicago</p>
							<p><b>Natalie Barnes ’19</b> expects to spend her career trying to improve big-city living conditions.  As a first-year student, the native of Cedar Rapids, Iowa, was accepted to a federally funded course in fair housing and fair lending practices at John Marshall Law School in Chicago.</p>
							<p>This summer the urban studies and political science major is attending a six-week course, Urban Studies: Methods and Policy, at the University of Chicago. The course brings together the theory and practice of urban studies, along with examinations of poverty, education, health, immigration, crime, energy and the environment. Barnes also will get to live in a University of Chicago dorm in Hyde Park.</p>
							<p>&ldquo;I’ll get to experience a Chicago neighborhood firsthand,&rdquo; she said.</p>
							<p>Barnes learned about the course earlier this year after Connie Mixon, director of Elmhurst’s Urban Studies program, sent students an email about it.</p>
							<p>&ldquo;I realized this is exactly what I want to do,&rdquo; she said.</p>
							<p>The emphasis on research methods will help her in future urban studies classes, as well as in professional work assessing problems and developing solutions.</p>
							<p>The class includes two days a week of class time and two days in an internship—Barnes will work at nonprofits Chicago Area Fair Housing Alliance and Housing Choice Partners. There’s also a weekly field trip to a Chicago neighborhood.</p>
							<p>&ldquo;It’s a down-deep, intensive course,&rdquo; she said. &ldquo;I’m looking forward to learning about Chicago neighborhoods and getting the perspectives of students from different parts of the country.&rdquo;</p>
