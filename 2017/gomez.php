							<h1>Tiny fossils tell big story of prehistoric environment</h1>
							<p class="ec-emphasis">Aspiring veterinarians learn to sift through ancient sediments in Keystone Program</p>
							<p>Fishing for microfossils in a petri dish of 60 million-year-old sediment from North Dakota was at first daunting for rising second-year students <b>Karla Gomez &rsquo;20 and Mariela Salgado &rsquo;20</b>. It took about a week for the pair to get comfortable spotting fragments of fish vertebrae, crocodile teeth and turtle shells. &ldquo;In the beginning we were so excited to find them,&rdquo; Gomez recalled. &ldquo;Then there were so many—it was just another one, then another one.&rdquo;</p>
							<p>Working on a research project with biology professor <b>Merrilee Guenther</b>, the students found and archived hundreds of specimens. Their mission: use the microfossils to reconstruct the local environment at the time. It turns out that the sediment was full of water-bound creatures, suggesting that this area of the northern Great Plains was aquatic. &ldquo;That was an aha moment,&rdquo; the students said. &ldquo;There were so many fish.&rdquo;</p>
							<p>This September, Gomez and Salgado will present their findings at The Keystone Summer Research Poster Session in the Frick Center, along with other Elmhurst students who conducted summer projects.</p>
							<p>Gomez, a biology major from Lindenhurst, Illinois, said her mother—a nurse—instilled a love of scientific investigation. She aims to pursue a career as a veterinarian. Salgado, a biology major from Franklin Park, Illinois, also wants to be a veterinarian.</p>
							<p>&ldquo;I used to read the encyclopedia for fun and animals always fascinated me,&rdquo; she said. The students received stipends for their research under <a href="http://www.elmhurst.edu/keystone" target="_blank">The Keystone Program</a>, a National Science Foundation supported program aimed at supporting STEM student success in the first year.</p>
