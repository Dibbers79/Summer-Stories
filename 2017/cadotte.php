							<h1>Flowing with Fish in Panama</h1>
							<p class="ec-emphasis">Science major tracks influence of currents in marine biology course</p>
							<p>When the ocean currents are swift, the fish go low. That was the scientific conclusion of <b>Aaron Cadotte &rsquo;18</b>, who traveled to Panama to study marine ecology, specifically, how fish adapt to water flows of different strengths.</p>
							<p>Cadotte, a physics and math major from Wheaton, Illinois, won a U.S. State Department-sponsored Gilman International Scholarship to study abroad. He enrolled in a six-week program, &ldquo;Panama: Comparative Marine Ecology and Blue Carbon Conversation,&rdquo; through the School for International Training that involved coursework, independent research, writing a paper and presenting the research results.</p>
							<p>To observe the fish, Cadotte’s cohort traveled to three islands off the Pacific coast of Panama. He swam about 100 meters off each island’s coast and took hundreds of photographs. &ldquo;The higher the current, the more the fish swan to the bottom of the sea floor,&rdquo; he said. &ldquo;It was easier for them to sit and eat there.&rdquo;</p>
							<p>Cadotte visited the famous Panama Canal, which has only recently been widened to allow huge containerships to pass from one great ocean to the other. One of his biggest takeaways: exposure to a completely different way of life, including widespread urban and rural poverty. &ldquo;I knew I’d come back with an appreciation of how good we have it,&rdquo; he said. &ldquo;I appreciate how good we have it.&rdquo;</p>
