							<h1>Reporting on Chicago’s Street Festivals</h1>
							<p class="ec-emphasis">Rising senior produces video stories for online magazine</p>
							<p><b>Marri Gragnani &rsquo;18</b> sampled ribs, burgers and music at Chicago street festivals this summer, but she didn’t attend just as a fan. She covered the events for the digital magazine <i>Rose Phillips</i>, which highlights and promotes local cultural events, small businesses and nonprofits.</p>
							<p>Gragnani, an interdisciplinary communications major from Berwyn, also is a forward on the Bluejays soccer team. She came upon Rose Phillips in an online search and worked for the publication during the winter. She was invited back for a seven-week summer internship.</p>
							<p>For each festival, she interviewed producers, publicists, vendors and attendees on camera. She also wrote the scripts, edited the video and uploaded her pieces to social media. Gragnani particularly enjoyed covering the Windy City Smokeout in mid-July, which featured country music and 13 barbecue pitmasters from around the country; and the Roscoe Village Burger Fest earlier in the month. &ldquo;The vendors wanted me to try their food, so I ate a lot of burgers,&rdquo; she said.</p>
							<p>Gragnani hopes to pursue a career in sports or entertainment broadcasting, and said the internship was a great training ground. &ldquo;It was fulfilling to see my name on the stories,&rdquo; she said. &ldquo;I got to do it all—you can’t learn these things in the classroom.&rdquo;</p>
