							<h1>The Importance of Whiskers</h1>
							<p class="ec-emphasis">Physics majors study how detection of air flows helps animals avoid predators</p>
							<p><b>Jordan Thomas &rsquo;18</b> wasn&rsquo;t enthusiastic about school until his brother suggested he read about astronomy. &ldquo;I got interested in how the universe works and started reading every popular science book at the store,&rdquo; he said.</p>
							<p>This summer Thomas is studying a question of biophysics: How whiskers help animals navigate their environments. Thomas and <b>Michael Griffin &rsquo;20</b> are working with physics professor <b>Venkatesh Gopal</b> to understand how these sensitive appendages detect air currents and, thereby, enable animals to avoid predators. Rather than use live animals, Gopal and his students collect data on how artificially made whiskers behave in the presence of different types of air flow.</p>
							<p>&ldquo;We believe that animals with sensitive whiskers are able to detect distinct air flow while ignoring the common air currents in their everyday environment,&rdquo; Thomas said. The physics major from Arlington Heights said he plans to continue studying the field in graduate school.</p>
							<p>Griffin, a physics major from Elmhurst, is participating in The Keystone Program, which provides stipends for rising sophomores to conduct research. &ldquo;I&rsquo;ve always been interested in exploring how things are built,&rdquo; said Griffin, who also plays bass trombone in the Elmhurst College Jazz Band. &ldquo;I used to take apart old cell phones.&rdquo;</p>
							<p>&ldquo;Next term he&rsquo;ll be running the experiments,&rdquo; Gopal said. Griffin added, &ldquo;I&rsquo;m in this for the duration.&rdquo;</p>
