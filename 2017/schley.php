							<h1>He Gets to Play Video Games All Day</h1>
							<p class="ec-emphasis">Internship provides rising senior with a window into product development</p>
							<p>Growing up in Northlake, Illinois, <b>Brandon Schley &rsquo;18</b> loved playing video games—Super Mario and the Battlefield series were among his favorites. This summer, Schley is exploring how he might parlay his childhood hobby into a career through an internship at game maker Team Play.</p>
							<p>The Elk Grove Village company manufactures arcade games and photo booths—the kinds found at entertainment venues such as Gameworks and Dave & Buster’s. During his eight-week internship, Schley has been testing new products for bugs and reporting back to the software programmers. He’s also been able to shadow the programmers, game designers and even the company owners.</p>
							<p>Schley likes to analyze games, mentally take them apart, and imagine how he might design them differently and make them better. Also, he enjoys coming up with characters that could extend a game series or anchor a new one.</p>
							<p>&ldquo;It’s been a unique opportunity to understand the game development process,&rdquo; he said. &ldquo;They’ve been super supportive and welcoming.&rdquo;</p>
							<p>A double major in computer science and gaming and entertainment, Schley began programming about three years ago.</p>
							<p>&ldquo;I enjoy the creativity and intricacy of programming and developing the visual elements,&rdquo; he said. &ldquo;And I’ve always enjoyed math.&rdquo;</p>
