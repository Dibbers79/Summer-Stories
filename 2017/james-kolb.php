							<h1>Tracing the Path of Pilgrims</i></h1>
							<p class="ec-emphasis">A senior studied terrain along historic route in Spain</p>
							<p>For centuries pilgrims have walked the Camino de Santiago for hundreds of miles through northwest Spain, to the shrine of the apostle St. James in the Cathedral of Santiago de Compostela. More recently, hikers and tourists have taken to the roads.</p>
							<p>This summer, <b>James Kolb &rsquo;17</b> (right in photo) was on the trail with 10 other Elmhurst College students as part of a study away course, Walking the Camino de Santiago: An Intellectual and Physical Journey. Taught by professors Beatriz Gomez-Acuna and Mick Savage, students can take the class for Spanish credit toward their major or minor or as an elective in kinesiology.</p>
							<p>Kolb and fellow student <b>Omar Vazquez &rsquo;18</b> (left in photo) are teaming up on an additional project—designing a multidisciplinary course for high school students, Discovering the Camino de Santiago. Kolb, who is majoring in physical education with a minor in health, was interested in accounting for the geography along the route, which runs along the coast and turns inland. “What will the terrain be like?” he said. “Will it be wet or dry?”</p>
							<p>The group walked 12 to 20 miles a day for 13 days. By tying a yellow scallop shell on their backpacks, hikers were able to stay at special hostels along the way, Kolb said.</p>
							<p>Kolb and Vazquez will present their work in October at the annual Symposium on Pilgrimage Studies at The College of William and Mary in Virginia, according to Gomez-Acuna. Kolb, a native of Rolling Meadows, plans to student-teach in the fall at a middle school in Arlington Heights.</p>
