							<h1>Studying the Art of the Memoir</h1>
							<p class="ec-emphasis">Honors Program Summer Fellow tackles the discipline of daily writing</p>
							<p><b>Evan Riley &rsquo;18</b> is studying the genre of memoir writing.</p>
							<p>After Riley took courses in news and feature writing as a junior, <b>Professor Ron Wiginton</b> encouraged the junior from Alma, Michigan, to branch out and try different styles. With an Honors Program Summer Fellowship, which carries a $1,500 stipend to support a summer research project, Riley is analyzing the work of author and humorist David Sedaris and other writers.</p>
							<p>Riley is deconstructing some of Sedaris&rsquo; works, including his memorable 2013 <i>New Yorker</i> essay &ldquo;Now We Are Five,&rdquo; about his sister&rsquo;s suicide. Part of Riley’s project is exploring the differences among narratives, essays and memoirs.</p>
							<p>&ldquo;He and I have been working on the concept of the &lsquo;memoir dilemma,&rsquo; essentially the question of how to tell a true story (journalism) but using the techniques of a fiction writer,&rdquo; Wiginton said.</p>
							<p>Riley’s goal is to produce a publishable memoir by the end of the summer. &ldquo;I want to see if I can make something of my own using what I’ve learned,&rdquo; he said. The fellowship has already had one impact: He’s considering changing his major from interdisciplinary communication studies to English in order read more literature.</p>
							<p>The project challenges Riley because as an independent study, there’s no formal structure. He tries to write frequently, even if it’s just tapping sentences into his phone.</p>
							<p>&ldquo;I know that if I want to pursue writing, I’m going to need discipline,&rdquo; he said. &ldquo;I’m using this as a test drive.&rdquo;</p>
