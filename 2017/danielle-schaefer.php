							<h1>Instructing Middle Schoolers in the Bronx</i></h1>
							<p class="ec-emphasis">Internship provides aspiring teacher training and feedback</p>
							<p><b>Danielle Schaefer ’18</b> gets to test her career plan this summer when she tutors 8th graders in math at a Bronx high school.</p>
							<p>The English and secondary education major from Lake Villa, Illinois, is participating in <a href="http://www.btny.org/" target="_blank">Breakthough New York</a>, a nonprofit that mentors low-income students and prepares them for college.</p>
							<p>&ldquo;I want to help kids who are disadvantaged and could use extra help,&rdquo; Schaefer said.</p>
							<p>Schaefer, who is active in her sorority Kappa Kappa Gamma, heard about the program at a Greek life leadership conference and said it sounded “amazing.” She filled out a lengthy application that required her to include a video of herself teaching a mock lesson.</p>
							<p>The nine-week program includes five weeks in the classroom with two morning math classes and a drama elective in the afternoon. That’s preceded by three weeks of training in lesson planning and classroom dynamics. The interns also receive a final week of evaluation. Schaefer is receiving free housing in a dorm at Long Island University in Brooklyn and a stipend to cover living expenses.</p>
							<p>While she has a lengthy commute to her school in the Bronx, she’s learning the ins and outs of New York City. She particularly enjoys exploring the city’s famed art museums.</p>
							<p>&ldquo;I could see myself living here,&rdquo; she said.</p>
