							<h1>The Benefit of an Early Start</i></h1>
							<p class="ec-emphasis">Planning ahead led to a rising senior's logistics internship</p>
							<p><b>Joe Garland ’18</b> is learning the ins and outs of international trade as part of his internship at Molex Inc., a giant maker of connectors and switches. Based in the company’s logistics department in Bolingbrook, Ill., he checks the paperwork accompanying imports to ensure the company is in compliance with customs regulations.</p>
							<p>&ldquo;I like the precision and problem-solving skills required,&rdquo; said the logistics and supply chain management major from Ottawa, Ill. &ldquo;How do you move things from point A to point B most efficiently?&rdquo;</p>
							<p>Garland will rotate into stints in inventory planning and logistics support that will give him a broad view of the department’s scope. He plans to continue working into the fall, when he’ll juggle football (he’s a tight end) and his class schedule. With so much to manage, he’s learned to be efficient.</p>
							<p>He started his internship search early in the year and got his name out to firms with area logistics operations. When Holly Coffin, assistant director for internships at the <a href="http://www.elmhurst.edu/cpe">Russell G. Weigand Center for Professional Excellence</a> visited the Molex distribution center in early winter, she learned that there were internship opportunities and passed the lead to Garland. He landed the gig in March and worked there part-time during Spring Term.</p>
							<p>&ldquo;His early preparation and search paid off,&rdquo; Coffin said.</p>
