							<h1>Playing Europe&rsquo;s Renowned Music Festivals</h1>
							<p class="ec-emphasis">Jazz Band members explore new venues and revisit old ones on summer tour</p>
							<p>For tenor saxophonist <b>Gabby Bandera &rsquo;19</b>, the highlight of the Elmhurst College Jazz Band&rsquo;s summer tour was the Umbria Jazz Festival in Perugia, Italy. There the 20-member ensemble performed with guest conductor Ryan Truesdell, a star composer and arranger, and Jazz master Dee Dee Bridgewater.</p>
							<p>&ldquo;Ryan had conducted two concerts with us and Dee Dee was a headliner at the [February] Jazz Festival, so we already had a connection,&rdquo; she said. &ldquo;It was a ton of fun.&rdquo;</p>
							<p>The band also performed at the Montreux Jazz Festival in Montreux, Switzerland; the Vienne Jazz Festival near Lyon, France; and other smaller venues.</p>
							<p>It was the second European tour for Bandera, a major in saxophone performance and music education from Oak Lawn, Illinois. &ldquo;It was cool to explore new locations and revisit old ones,&rdquo; she said. Bandera began playing clarinet in fourth grade and sax in middle school. Professionally, she plans to teach younger students and continue performing.</p>
							<p>For trumpeter <b>Adam Roebuck</b>, the trip was bittersweet since he recently graduated. Roebuck, a major in jazz studies and music business from Columbia, Missouri, started trumpet in the sixth grade. He plans to teach as well as write and perform his own music.</p>
							<p>&ldquo;It’s amazing having the opportunity to play at some of the world’s most renowned music festivals,&rdquo; Roebuck said. &ldquo;Concert attendees, as well as other performers, always are impressed to hear a big band comprised of such young musicians performing at such a high level."</p>
