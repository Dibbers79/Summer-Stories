							<h1>Serving Students in South Africa</h1>
							<p class="ec-emphasis">Rebecca Blaufuss and Molly Brennan add Honors challenge to trip</p>
							<p><b>Molly Brennan &rsquo;19</b> recalls being impressed when she learned that her friend’s sister was an engineer building an irrigation system in South Africa. &ldquo;I realized that I didn’t have to be a doctor to help people, she said. &ldquo;I could be an engineer.&rdquo;</p>
							<p>Brennan helped young students in Cape Town as part of an Elmhurst College service learning course, South Africa Service and Interdisciplinary Study. The students were assigned either to a preschool or elementary school for 10 days, or worked with the Mustadafin Foundation, a community organization that deals with humanitarian and social issues. The trip concluded with a visit to a wildlife preserve.</p>
							<p>Brennan and <b>Rebecca Blaufuss &rsquo;18</b> each did an extra project to earn Honors credit.</p>
							<p>Blaufuss, an elementary education major from Cleveland, Ohio, wrote a paper comparing South African and U.S. schools in the methods and standards used to teach math. &ldquo;I [was] interested in getting exposure to another country’s education system,&rdquo; she said.</p>
							<p>Brennan, a computer science major who grew up in Chicago suburb of Tinley Park, planned a project on the history of South Africa’s music culture. She was inspired by education professor <b>Therese Wehman</b>, one of three faculty members who led the trip.</p>
							<p>&ldquo;This is once in a lifetime, an experience that will change your life,&rdquo; she recalled Wehman saying. &ldquo;I had a gut feeling that I needed to do this.&rdquo;</p>
