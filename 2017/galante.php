							<h1>A Window into Sports Management</h1>
							<p class="ec-emphasis">Miami Dolphins internship offers inside look at team operations</p>
							<p><b>Christian Galante &rsquo;19</b>, who grew up in the Florida city of Plantation near Fort Lauderdale, was always a Miami Dolphins fan. He attended home games with his family and even volunteered at the team’s summer training camp during high school.</p>
							<p>This year he applied for the Dolphins’ paid internship program, which required an application and interview. For the first part of the program, he helped coach in the team’s flag football youth camps. For the second half, he’s shifting gears and working at the Dolphins’ training facility at Nova Southeastern University in Davie. There he’s handling a range of tasks, from setting up and breaking down equipment to helping fans find seats, food, and souvenirs.</p>
							<p>Galante is majoring in business administration with a minor in coaching, and plays lacrosse at the College. He says the internship should give him a window into sports management, which he plans to pursue as a career.</p>
