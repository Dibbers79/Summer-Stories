							<h1>Advocating for Gender Equality</h1>
							<p class="ec-emphasis">Rising senior gains valuable career advice during Women Employed internship</p>
							<p>For <b>Ana Pliego &rsquo;18</b>, a Spring Term course, Gender and Communication, opened a window into the problem of gender inequality. When she learned that the Chicago nonprofit Women Employed offered a summer internship program, she seized the opportunity.</p>
							<p>Pliego, a psychology and organizational communication major from Wheaton, is one of 10 interns participating in the eight-week program. Founded in 1973, Women Employed advocates for policies that enable women to advance in the workplace, such as outlawing sexual harassment and pregnancy discrimination.</p>
							<p>As part of her internship, Pliego has researched potential partners and sponsors for Women Employed, including the 100 businesses that signed President Barack Obama’s 2014 executive action to strengthen enforcement of equal pay laws for women.</p>
							<p>One high point has been Women Employed bringing in guest speakers—from nonprofits and city government—to share their career experiences with the interns. Pliego said she and the other interns have appreciated informal conversations with staff members who offer advice on getting started in careers.</p>
							<p>&ldquo;They’ve suggested that I don’t go right to graduate school if I’m not sure what I want to do, which makes sense,&rdquo; Pliego said. &ldquo;They said to get to know yourself first.&rdquo;</p>
							<p>As a result, Pliego said she plans to gain job experience after she graduates from Elmhurst next year and focus on graduate school later.</p>
