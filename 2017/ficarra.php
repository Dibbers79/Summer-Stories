							<h1>In Search of Elusive Koalas</h1>
							<p class="ec-emphasis">In Queensland program, a rising junior explores the animal’s decline</p>
							<p>Koalas, the shy and cuddly Australian marsupials, are in trouble. <b>Georgia Ficarra &rsquo;19</b> is exploring reasons behind the decline of their populations as part of a study away program at the University of Queensland in Brisbane.</p>
							<p>Ficarra, a biology major from Bloomingdale, Illinois, sought a summer research project involving animals.  Analyzing data gathered by her professor, she found that disease, the loss of trees and urbanization have devastated the koalas.</p>
							<p>&ldquo;Massive deforestation is causing a severe loss of eucalyptus trees, particularly blue gums that are koalas&rsquo; favorite food,&rdquo; Ficarra said.</p>
							<p>Her field work has taken her to bushland reservations as well as neighborhood parks in search of the animals. Vegetation, land elevation and phosphorus levels indicate whether koalas are likely to be around. These elements are important for prioritizing the land that gets preserved for koala use, Ficarra said.</p>
							<p>She and other students studying at the university through Arcadia University’s study away program have enjoyed surfing, kayaking and other excursions, including a trip to the Lone Pine Koala Sanctuary, where she held koalas and fed kangaroos.</p>
							<p>Her favorite experience has been the exposure to Australian plants and animals—not just koalas, but rainbow lorikeets (a species of parrot), wallabies and lizards.</p>
							<p>&ldquo;Australia is interesting because while on a city street you may feel like you're in America,&rdquo; Ficarra said. &ldquo;Then the minute you see a bird or native tree you think, wow, that's different.&rdquo;</p>
